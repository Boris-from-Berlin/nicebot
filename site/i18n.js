// ─── i18n Engine ──────────────────────────────────────────
let currentLang = 'en';

function t(key) {
  const lang = LANGS[currentLang];
  if (!lang) return key;
  return lang.translations[key] || LANGS.en.translations[key] || key;
}

function setLang(code) {
  if (!LANGS[code]) code = 'en';
  currentLang = code;
  const lang = LANGS[code];

  // Update dir and lang on html element
  document.documentElement.dir = lang.dir;
  document.documentElement.lang = code;

  // Update all data-i18n elements (textContent)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) el.textContent = val;
  });

  // Update all data-i18n-html elements (innerHTML)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    const val = t(key);
    if (val !== key) el.innerHTML = val;
  });

  // Store preference
  localStorage.setItem('nicebot-lang', code);

  // Notify other scripts
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: code } }));
}

function getCurrentLang() { return currentLang; }

// Auto-detect language on load
(function() {
  const stored = localStorage.getItem('nicebot-lang');
  if (stored && LANGS[stored]) {
    currentLang = stored;
  } else {
    const browser = (navigator.language || 'en').split('-')[0];
    currentLang = LANGS[browser] ? browser : 'en';
  }
})();
